function parseArgs(args: string[]): Options {
  if (args.includes("--help") || args.includes("-h")) {
    console.log(`Usage:
  pnpm arcade:generate <toolkit> [--out <dir>] [--snapshot <file>] [--force]

Examples:
  pnpm arcade:generate gmail --out packages/integrations/gmail-generated
  pnpm arcade:generate gmail --snapshot .context/arcade-gmail-tools.json --force

The first run fetches /v1/tools from Arcade and writes the snapshot. Later runs
from the same snapshot are deterministic.`);
    process.exit(0);
  }

  const toolkit = args.find((arg) => !arg.startsWith("-"));
  if (!toolkit) throw new Error("Missing toolkit name, for example: gmail");

  const getFlag = (name: string): string | undefined => {
    const index = args.indexOf(name);
    return index === -1 ? undefined : args[index + 1];
  };

  const toolkitSlug = slugify(toolkit);
  return {
    toolkit,
    outDir:
      getFlag("--out") ?? path.join("packages", "integrations", toolkitSlug),
    snapshotPath:
      getFlag("--snapshot") ??
      path.join(".context", `arcade-${toolkitSlug}-tools.json`),
    apiKey: getFlag("--api-key"),
    baseUrl: getFlag("--base-url") ?? "https://api.arcade.dev",
    packageName: getFlag("--package-name"),
    force: args.includes("--force"),
    noFetch: args.includes("--no-fetch"),
  };
}

function stableJson(value: unknown): string {
  return `${JSON.stringify(sortJson(value), null, 2)}\n`;
}

function sortJson(value: unknown): unknown {
  if (Array.isArray(value)) return value.map(sortJson);
  if (!value || typeof value !== "object") return value;
  return Object.fromEntries(
    Object.entries(value as Record<string, unknown>)
      .sort(([left], [right]) => left.localeCompare(right))
      .map(([key, item]) => [key, sortJson(item)]),
  );
}

function slugify(value: string): string {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function pascalCase(value: string): string {
  return value
    .split(/[^a-zA-Z0-9]+|(?=[A-Z])/)
    .filter(Boolean)
    .map((part) => part.slice(0, 1).toUpperCase() + part.slice(1))
    .join("");
}

function camelCase(value: string): string {
  return lowerFirst(pascalCase(value));
}

function uniqueLowerCamel(value: string): string {
  const result = camelCase(value);
  return result.length === 0 ? "tool" : result;
}

function lowerFirst(value: string): string {
  return value.slice(0, 1).toLowerCase() + value.slice(1);
}

function constantName(value: string): string {
  return slugify(value).replace(/-/g, "_").toUpperCase();
}

function stripToolkitPrefix(value: string, toolkitName: string): string {
  return value.replace(new RegExp(`^${escapeRegExp(toolkitName)}\\.`), "");
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function quote(value: string): string {
  return JSON.stringify(value);
}

