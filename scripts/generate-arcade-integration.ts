function normalizeTools(tools: ToolResponse[]): ToolResponse[] {
  return [...tools].sort((left, right) =>
    (left.fully_qualified_name ?? left.qualified_name).localeCompare(
      right.fully_qualified_name ?? right.qualified_name,
    ),
  );
}

