function collectEnumSchemas(
  toolkitPascal: string,
  tools: ToolResponse[],
): Map<string, { typeName: string; schemaName: string; definition: string }> {
  const schemas = new Map<
    string,
    { typeName: string; schemaName: string; definition: string }
  >();
  const usedTypeNames = new Set<string>();

  for (const tool of tools) {
    const parameters = normalizeParameters(tool.input?.parameters ?? []);
    for (const parameter of parameters) {
      const values = parameter.value_schema.enum;
      if (!values || values.length === 0) continue;
      const key = [...values].sort().join("\0");
      if (schemas.has(key)) continue;

      const baseTypeName = `${toolkitPascal}${pascalCase(parameter.name)}`;
      let typeName = baseTypeName;
      if (usedTypeNames.has(typeName)) {
        typeName = `${toolkitPascal}${pascalCase(tool.name)}${pascalCase(parameter.name)}`;
      }
      let suffix = 2;
      while (usedTypeNames.has(typeName)) {
        typeName = `${baseTypeName}${suffix}`;
        suffix += 1;
      }
      usedTypeNames.add(typeName);

      const schemaName = `${lowerFirst(typeName)}Schema`;
      schemas.set(key, {
        typeName,
        schemaName,
        definition: `export const ${schemaName} = z.enum([${values
          .map(quote)
          .join(
            ", ",
          )}]);\nexport type ${typeName} = z.infer<typeof ${schemaName}>;\n`,
      });
    }
  }

  return schemas;
}

function schemaToType(
  schema: ValueSchema | undefined,
  toolkitPascal: string,
): string {
  if (!schema) return `${toolkitPascal}JsonObject`;
  if (schema.enum && schema.enum.length > 0) {
    return schema.enum.map(quote).join(" | ");
  }

  switch (schema.val_type.toLowerCase()) {
    case "string":
    case "str":
      return "string";
    case "integer":
    case "int":
    case "number":
    case "float":
      return "number";
    case "boolean":
    case "bool":
      return "boolean";
    case "array":
    case "list":
      return `${primitiveType(schema.inner_val_type)}[]`;
    case "object":
    case "dict":
    case "json":
      return "Record<string, unknown>";
    default:
      return "unknown";
  }
}

function schemaToZod(schema: ValueSchema | undefined): string {
  if (!schema) return "jsonObjectSchema";
  if (schema.enum && schema.enum.length > 0) {
    return `z.enum([${schema.enum.map(quote).join(", ")}])`;
  }

  switch (schema.val_type.toLowerCase()) {
    case "string":
    case "str":
      return "z.string()";
    case "integer":
    case "int":
      return "z.number().int()";
    case "number":
    case "float":
      return "z.number()";
    case "boolean":
    case "bool":
      return "z.boolean()";
    case "array":
    case "list":
      return `z.array(${primitiveZod(schema.inner_val_type)})`;
    case "object":
    case "dict":
    case "json":
      return "jsonObjectSchema";
    default:
      return "z.unknown()";
  }
}

